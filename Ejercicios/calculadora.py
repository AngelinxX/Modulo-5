alto=float(input("Ingresar el alto del rectangulo"))
ancho=float(input("Ingresar el ancho del rectangulo"))

area_del_rectangulo = (alto * ancho)
perimetro_del_rectangulo = (alto*2 + ancho*2)
superficie_del_rectangulo = (ancho * alto)

print(f"Area:{area_del_rectangulo}")
print(f"Perimetro:{perimetro_del_rectangulo}")
print(f"Superficie: {superficie_del_rectangulo}")
